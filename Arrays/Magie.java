package Java.Arrays;

public class Magie {
  
  private String name;
  private int quantity;
  
  public Magie(String name,int quantity){
    
    this.name=name;
    this.quantity=quantity;
  }
  public String getName(){
    
    return this.name;
  }
  public int getQuantity(){
    
    return this.quantity;
  }

}
