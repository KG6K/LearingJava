package Java.IfAndElse;

public class Shoe {
  
  private String Colour;
  private int Size;
  
  public Shoe(String Colour,int size){
    
    this.Colour=Colour;
    this.Size=size;
  }
  public String getColour(){
    
    return this.Colour;
  }
  public int getSize(){
    
    return this.Size;
  }
}
